# Metodos
def soma(n1, n2):
    """
    Soma dois números inteiros.
    :param n1: Primeiro número inteiro.
    :param n2: Segundo número inteiro.
    :return: A soma dos dois números inteiros.
    """
    return n1 + n2
print (soma(5, 5))


